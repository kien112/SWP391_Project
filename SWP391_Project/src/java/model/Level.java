package model;

/**
 *
 * @author 84877
 */
public class Level {
    private int id;
    private String level;
    private boolean status;

    public Level() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getLevel() {
        return level;
    }

    public void setLevel(String level) {
        this.level = level;
    }

    public boolean isStatus() {
        return status;
    }

    public void setStatus(boolean status) {
        this.status = status;
    }

    @Override
    public String toString() {
        return "Level{" + "id=" + id + ", level=" + level + ", status=" + status + '}';
    }
    
    
}
